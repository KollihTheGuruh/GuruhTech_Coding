# Hello World Program
# A simple program that prints "Hello, World!" to the console.
print("Hello, World!")

# Variables and Data Types
# Variables are used to store data.
# Python supports various data types like integers, floats, and strings.

# Integer variable
x = 5
print("Integer variable:", x)

# Float variable
y = 3.14
print("Float variable:", y)

# String variable
name = "Alice"
print("String variable:", name)

# Conditional Statements
# Conditional statements allow us to make decisions in our code.
# We'll use an "if" statement as an example.

age = 20

if age >= 18:
    print("You are an adult.")
else:
    print("You are a minor.")

# Loops
# Loops are used to repeat a block of code.
# We'll demonstrate a "for" loop.

fruits = ["apple", "banana", "cherry"]

print("Fruits in the list:")
for fruit in fruits:
    print(fruit)

# Functions
# Functions allow us to group code into reusable blocks.
# We'll create a simple function.

def greet(name):
    print("Hello, " + name + "!")

greet("Bob")

